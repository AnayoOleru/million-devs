<script>
import { gsap } from "gsap";
import { mapState } from "vuex";
import { ScrollTrigger } from "gsap/ScrollTrigger.js";
import { MorphSVGPlugin } from "gsap/MorphSVGPlugin.js";

gsap.registerPlugin(ScrollTrigger);
gsap.registerPlugin(MorphSVGPlugin);

export default {
  computed: {
    ...mapState(["toggleConfig", "startConfig"])
  },
  methods: {
    bookAnim() {
      gsap
        .timeline({
          scrollTrigger: {
            trigger: "#book",
            toggleActions: this.toggleConfig,
            start: this.startConfig
          }
        })
        .add("book")
        .to(
          "#bookmark",
          {
            duration: 0.5,
            morphSVG: "#bookmark-morph",
            repeat: 3,
            yoyo: true,
            ease: "sine.inOut"
          },
          "book"
        )
        .to(
          "#thumb1",
          {
            duration: 0.5,
            rotation: 15,
            repeat: 3,
            yoyo: true,
            ease: "sine.inOut"
          },
          "book+=0.25"
        )
        .to(
          "#thumb2",
          {
            duration: 0.5,
            rotation: 15,
            repeat: 3,
            yoyo: true,
            ease: "sine.inOut"
          },
          "book+=0.25"
        )
        .from(
          ".like-boxes",
          {
            duration: 0.5,
            scale: 0,
            opacity: 0,
            rotation: 180,
            transformOrigin: "50% 50%",
            stagger: 0.1,
            ease: "sine"
          },
          "book+=0.5"
        );
    }
  },
  mounted() {
    this.bookAnim();
  }
};
