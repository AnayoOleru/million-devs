import { ScrollTrigger } from "gsap/ScrollTrigger.js";
import { mapState } from "vuex";

gsap.registerPlugin(ScrollTrigger);

export default {
  computed: {
    ...mapState(["toggleConfig", "startConfig", "isAnimationDisabled"]),
  },
  methods: {
    bookAnim() {
      gsap
        .timeline({
          scrollTrigger: {
            trigger: "#functions",
            toggleActions: this.toggleConfig,
            start: this.startConfig,
          },
        })
        .add("functions")
        .from(
          ".sheet",
          {
            duration: 1,
            scale: 0,
            opacity: 0,
            rotation: 360,
            transformOrigin: "50% 50%",
            stagger: 0.1,
            ease: "sine",
          },
          "functions"
        )
        .from(
          ".bloop",
          {
            duration: 1,
            scale: 0,
            opacity: 0,
            rotation: -360,
            transformOrigin: "50% 50%",
            stagger: 0.1,
            ease: "sine",
          },
          "functions+=0.5"
        );
    },
  },
  mounted() {
    if (!this.isAnimationDisabled) {
      this.bookAnim();
    }
  },
};
